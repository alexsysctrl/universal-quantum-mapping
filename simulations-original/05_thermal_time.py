# =============================================================================

def entropy_time_arrow():
    """Show that the arrow of time emerges from entropy.
    
    In thermal time hypothesis:
      - Low entropy state → strong time flow (fast modular evolution)
      - High entropy state → weak time flow (slow modular evolution)
      - Maximum entropy (thermal equilibrium) → no time flow (static)
    """
    system = ThermalTimeSystem(n=4)
    
    # Entropy vs time flow rate
    betas = np.linspace(0.01, 10, 100)
    entropies = []
    time_rates = []
    
    for beta in betas:
        rho, _ = system.thermal_state(beta)
        
        # Von Neumann entropy
        eigenvalues = np.linalg.eigvalsh(rho)
        eigenvalues = eigenvalues[eigenvalues > 1e-10]
        S = -np.sum(eigenvalues * np.log(eigenvalues))
        entropies.append(S)
        
        # Time flow rate (modular Hamiltonian eigenvalue spread)
        K = system.modular_hamiltonian(rho)
        eigenvalues_K = np.linalg.eigvalsh(K)
        rate = np.max(eigenvalues_K) - np.min(eigenvalues_K)
        time_rates.append(rate)
    
    return betas, entropies, time_rates


