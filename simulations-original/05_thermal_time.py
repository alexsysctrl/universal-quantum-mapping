# Modular Automorphism Group (Finite-Dimensional Approximation)
# =============================================================================

class ThermalTimeSystem:
    """Simulate the Connes-Rovelli thermal time hypothesis.
    
    In a finite-dimensional approximation:
      - Algebra: M_n(C) (n×n complex matrices)
      - State: density matrix ρ
      - Modular Hamiltonian: H = -log(ρ)
      - Modular group: σ_t(A) = e^{itH} A e^{-itH}
      - Time evolution is generated by H, which depends on the state ρ
    
    Key insight: There is NO preferred time evolution.
    Time emerges from the choice of state.
    """
    
    def __init__(self, n=4):
        """
        Args:
            n: Dimension of the algebra (n×n matrices)
        """
        self.n = n
    
    def thermal_state(self, beta):
        """Create a thermal (Gibbs) state at inverse temperature β.
        
        ρ = e^{-βH_0} / Z where H_0 is a fixed reference Hamiltonian.
        """
        # Reference Hamiltonian: diagonal with sorted positive eigenvalues
        eigenvalues_H = np.sort(np.random.exponential(size=self.n)) + 0.1
        H0 = np.diag(eigenvalues_H)
        
        # Thermal state: ρ = e^{-βH_0} / Z
        rho = np.exp(-beta * H0)
        rho = rho / np.trace(rho)  # Normalize to trace 1
        
        return rho, H0
    
    def modular_hamiltonian(self, rho):
        """Compute the modular Hamiltonian K = -log(ρ).
        
        This is the generator of thermal time evolution.
        The state ρ determines the time flow.
        """
        eigenvalues, eigenvectors = np.linalg.eigh(rho)
        # Clamp eigenvalues to avoid log(0) and negative values
        eigenvalues = np.maximum(eigenvalues, 1e-10)
        log_eigs = -np.log(eigenvalues)
        
        # Ensure correct shapes
        if log_eigs.ndim == 0:
            log_eigs = np.array([log_eigs])
        if eigenvectors.ndim == 1:
            eigenvectors = eigenvectors.reshape(-1, 1)
        
        K = eigenvectors @ np.diag(log_eigs) @ eigenvectors.conj().T
        K = (K + K.conj().T) / 2  # Ensure Hermitian
        
        return K
    
    def modular_evolution(self, A, rho, t):
        """Compute modular automorphism: σ_t(A) = e^{itK} A e^{-itK}.
        
        This is the time evolution of observable A in thermal time.
        """
        K = self.modular_hamiltonian(rho)
        
        # Matrix exponential: e^{itK}
        eigenvalues_K, eigenvectors_K = np.linalg.eigh(K * t)
        exp_itK = eigenvectors_K @ np.diag(np.exp(1j * eigenvalues_K)) @ eigenvectors_K.T
        
        # Evolution: σ_t(A) = e^{itK} A e^{-itK}
        A_evolved = exp_itK @ A @ exp_itK.conj().T
        
        return A_evolved
    
    def kms_condition(self, A, B, rho, beta, tolerance=1e-6):
        """Test the KMS (Kubo-Martin-Schwinger) condition.
        
        ω(A σ_{iβ}(B)) = ω(BA)
        
        For a thermal state ρ = e^{-βH}/Z, the KMS condition is automatically satisfied.
        We verify this numerically.
        """
        # For thermal state ρ = e^{-βH}/Z, we have K = βH
        # σ_{iβ}(B) = e^{-βK} B e^{βK} = e^{-β²H} B e^{β²H}
        # But the standard KMS condition uses the modular group:
        # σ_t(A) = e^{itK} A e^{-itK} where K = -log(ρ)
        # The KMS condition: <A(t+iβ) B> = <BA>
        
        # For our thermal state, K = -log(ρ) = βH + log(Z)
        # Since log(Z) is a constant, it cancels in the commutator
        # So σ_{iβ}(B) = e^{-βK} B e^{βK} = e^{-β²H} B e^{β²H}
        
        # Simpler approach: for thermal state, KMS is satisfied by construction
        # Just verify numerically with the modular Hamiltonian
        K = self.modular_hamiltonian(rho)
        
        # Compute σ_{iβ}(B) = e^{-i(iβ)K} B e^{i(iβ)K} = e^{βK} B e^{-βK}
        eigenvalues_K, eigenvectors_K = np.linalg.eigh(K)
        exp_beta_K = eigenvectors_K @ np.diag(np.exp(beta * eigenvalues_K)) @ eigenvectors_K.conj().T
        exp_minus_beta_K = eigenvectors_K @ np.diag(np.exp(-beta * eigenvalues_K)) @ eigenvectors_K.conj().T
        
        sigma_iB = exp_beta_K @ B @ exp_minus_beta_K
        
        # ω(A σ_{iβ}(B)) = Tr(ρ A σ_{iβ}(B))
        lhs = np.real(np.trace(rho @ A @ sigma_iB))
        
        # ω(BA) = Tr(ρ BA)
        rhs = np.real(np.trace(rho @ B @ A))
        
        return abs(lhs - rhs) < tolerance, lhs, rhs
    
    def thermal_time_flow(self, rho, observables, t_max=10.0, n_steps=200):
        """Compute time evolution of observables in thermal time.
        
        Different states (ρ) give different time flows.
        """
        rho_copy = np.array(rho, dtype=np.complex128)  # Defensive copy
        K = self.modular_hamiltonian(rho_copy)
        times = np.linspace(0, t_max, n_steps)
        
        evolution = []
        for t in times:
            evolved = []
            for A in observables:
                A_evolved = self.modular_evolution(A, rho, t)
                # Track expectation value: ⟨A⟩_t = Tr(ρ A(t))
                expectation = np.real(np.trace(rho @ A_evolved))
                evolved.append(expectation)
            evolution.append(evolved)
        
        return times, np.array(evolution)


