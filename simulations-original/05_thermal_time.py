def compare_time_flows():
    """Show that different thermal states give different time flows.
    
    This is the core prediction of the thermal time hypothesis:
    time is not absolute; it depends on the state of the system.
    """
    system = ThermalTimeSystem(n=4)
    
    # Different temperatures
    betas = [0.1, 0.5, 1.0, 2.0, 5.0]  # β = 1/T
    
    results = {}
    for beta in betas:
        rho, H0 = system.thermal_state(beta)
        K = system.modular_hamiltonian(rho)
        
        # Observable: random Hermitian matrix
        A = np.random.randn(system.n, system.n)
        A = (A + A.T) / 2
        
        # Evolution
        times, evolution = system.thermal_time_flow([A], rho, t_max=5.0, n_steps=200)
        
        # Characteristic frequency from modular Hamiltonian
        freq = np.max(np.abs(np.linalg.eigvalsh(K)))
        
        results[beta] = {
            'rho': rho,
            'K': K,
            'times': times,
            'evolution': evolution[0],
            'frequency': freq,
        }
    
    return betas, results


def thermal_time_vs_classical_time():
    """Compare thermal time with classical (Newtonian) time.
    
    Classical time: absolute, independent of state
    Thermal time: emergent, depends on state
    """
    system = ThermalTimeSystem(n=4)
    
    # Classical time: fixed evolution
    H_classical = np.diag([0, 1, 2, 3])
    times_classical = np.linspace(0, 10, 200)
    
    # Thermal time: state-dependent evolution
    betas = [0.5, 1.0, 2.0]
    
    results = {}
    for beta in betas:
        rho, _ = system.thermal_state(beta)
        K = system.modular_hamiltonian(rho)
        
        # Compute "time dilation" factor
        # Thermal time τ = βt (dimensionless)
        # Compare with classical time
        eigenvalues_K = np.linalg.eigvalsh(K)
        max_abs = np.max(np.abs(eigenvalues_K))
        time_scale = 1.0 / max_abs if max_abs > 0 else 1.0
        
        results[beta] = {
            'time_scale': time_scale,
            'eigenvalues': eigenvalues_K,
        }
    
    return times_classical, betas, results


