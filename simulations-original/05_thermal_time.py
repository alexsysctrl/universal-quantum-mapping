def plot_kms_condition():
    """Visualize the KMS condition as the defining property of thermal time."""
    fig, axes = plt.subplots(1, 2, figsize=(14, 6))
    
    # 1. KMS condition verification
    ax = axes[0]
    system = ThermalTimeSystem(n=4)
    
    betas_test = [0.5, 1.0, 2.0]
    kms_violations = []
    
    for beta in betas_test:
        rho, _ = system.thermal_state(beta)
        
        # Generate random observables
        violations = []
        for _ in range(20):
            A = np.random.randn(system.n, system.n)
            A = (A + A.T) / 2
            B = np.random.randn(system.n, system.n)
            B = (B + B.T) / 2
            
            satisfied, lhs, rhs = system.kms_condition(A, B, rho, beta)
            violations.append(abs(lhs - rhs))
        
        kms_violations.append(np.mean(violations))
    
    bars = ax.bar([f'β={b:.1f}' for b in betas_test], kms_violations,
                 color=['steelblue', 'coral', 'green'], edgecolor='black', linewidth=2)
    
    for bar, v in zip(bars, kms_violations):
        ax.text(bar.get_x() + bar.get_width()/2, bar.get_height() + 1e-6,
               f'{v:.2e}', ha='center', va='bottom', fontsize=11, fontweight='bold')
    
    ax.set_ylabel('KMS Violation |ω(Aσ_{iβ}(B)) - ω(BA)|', fontsize=11)
    ax.set_title('KMS Condition Verification\n(Should be ≈ 0 for thermal states)', fontsize=13)
    ax.set_ylim(0, max(kms_violations) * 5)
    ax.grid(True, alpha=0.3, axis='y')
    
    # 2. Modular flow diagram
    ax = axes[1]
    
    # Draw the modular group action
    t_values = np.linspace(-5, 5, 100)
    
    # Observable expectation value under modular flow
    rho, _ = system.thermal_state(1.0)
    A = np.diag([1, 0.5, 0.3, 0.1])
    
    expectations = []
    for t in t_values:
        A_t = system.modular_evolution(A, rho, t)
        exp_val = np.real(np.trace(rho @ A_t))
        expectations.append(exp_val)
    
    ax.plot(t_values, expectations, 'b-', linewidth=3)
    ax.axvline(x=0, color='k', linestyle='--', alpha=0.3)
    ax.axhline(y=np.trace(rho @ A), color='r', linestyle='--', alpha=0.5,
              label='Initial ⟨A⟩')
    ax.set_xlabel('Modular Time t', fontsize=12)
    ax.set_ylabel('⟨A⟩_t = Tr(ρ e^{itK} A e^{-itK})', fontsize=11)
    ax.set_title('Modular Automorphism Group\n(σ_t generates time evolution)', fontsize=13)
    ax.legend()
    ax.grid(True, alpha=0.3)
    
    plt.tight_layout()
    plt.savefig(os.path.join(OUTPUT_DIR, '18_kms_condition.png'), bbox_inches='tight')
    plt.close()


